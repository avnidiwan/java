public class checkVowelOrConsonent {
    public static void main(String[] args) {
        char alp='s';
        if((alp=='a')||(alp=='A')||(alp=='e')||(alp=='E')||alp=='i'||alp=='I'||alp=='o'||alp=='O'||alp=='u'||alp=='U')
        
            System.out.println(alp+" is a VOWEL");
        
        else{
            System.out.println(alp+" is a CONSONENT");
        }
    }
}
